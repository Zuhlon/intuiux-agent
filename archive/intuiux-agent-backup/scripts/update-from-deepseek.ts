async function updateAgentsWithKnowledge() {
  console.log('🔄 Updating agents with DeepSeek chat knowledge...');

  // 1. Добавляем новые знания
  for (const knowledge of NEW_KNOWLEDGE) {
    const existing = await db.knowledgeBase.findFirst({
      where: { title: knowledge.title }
    });

    if (!existing) {
      await db.knowledgeBase.create({
        data: {
          ...knowledge,
          tags: JSON.stringify(knowledge.tags)
        }
      });
      console.log(`✅ Added knowledge: ${knowledge.title}`);
    } else {
      console.log(`⏭️ Knowledge exists: ${knowledge.title}`);
    }
  }

  // 2. Обновляем промпт исследователя
  const researcher = await db.agent.findFirst({
    where: { type: 'researcher' }
  });

  if (researcher) {
    await db.agent.update({
      where: { id: researcher.id },
      data: { systemPrompt: AGENT_UPDATES.researcher.systemPrompt }
    });
    console.log('✅ Updated researcher prompt');
  }

  // 3. Добавляем к промпту аналитика
  const analyst = await db.agent.findFirst({
    where: { type: 'analyst' }
  });

  if (analyst) {
    await db.agent.update({
      where: { id: analyst.id },
      data: {
        systemPrompt: analyst.systemPrompt + AGENT_UPDATES.analyst.additionalPrompt
      }
    });
    console.log('✅ Updated analyst prompt');
  }

  console.log('🎉 Knowledge update complete!');
}

updateAgentsWithKnowledge()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error('❌ Update failed:', error);
    process.exit(1);
  });
