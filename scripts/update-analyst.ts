async function updateAnalyst() {
  console.log('🔄 Updating UX Analyst...');

  // Обновляем промпт аналитика
  const analyst = await db.agent.updateMany({
    where: { type: 'analyst' },
    data: { systemPrompt: ANALYST_PROMPT }
  });
  console.log(`✅ Updated ${analyst.count} analyst(s)`);

  // Добавляем новые знания о метриках
  for (const knowledge of METRICS_KNOWLEDGE) {
    const existing = await db.knowledgeBase.findFirst({
      where: { title: knowledge.title }
    });

    if (!existing) {
      await db.knowledgeBase.create({
        data: {
          ...knowledge,
          tags: JSON.stringify(knowledge.tags)
        }
      });
      console.log(`✅ Added knowledge: ${knowledge.title}`);
    } else {
      console.log(`⏭️ Knowledge already exists: ${knowledge.title}`);
    }
  }

  console.log('🎉 Update complete!');
}

updateAnalyst()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error('❌ Update failed:', error);
    process.exit(1);
  });
